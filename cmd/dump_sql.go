package cmd

import (
	"github.com/spf13/cobra"
)

var (
	databaseType string
	pending      bool
)

// dumpSQLCmd represents the dump_sql command
var dumpSQLCmd = &cobra.Command{
	Use:   "dump_sql",
	Short: "Dump schema as SQL to console",
	Long: `Dump schema as SQL to the console.

Without --pending: Dumps the complete merged schema as CREATE TABLE statements.
With --pending: Shows only the SQL that would be generated by the next migration
(ALTER TABLE, ADD COLUMN, DROP TABLE, etc.) based on changes between the current
YAML schema and the last applied migration state.

This command scans all YAML schema files in Go module dependencies and merges them
into a unified schema. In full mode, it outputs the complete SQL. In pending mode,
it diffs the current schema against the state from existing migrations and outputs
only the SQL for the detected changes.

Database Support:
- PostgreSQL (default)
- MySQL
- SQL Server
- SQLite`,
	RunE: func(cmd *cobra.Command, args []string) error {
		return runDumpSQL(cmd, args)
	},
}

// runDumpSQL executes the dump_sql command
func runDumpSQL(cmd *cobra.Command, args []string) error {
	return ExecuteDumpSQL(cmd, databaseType, pending, verbose)
}

func init() {
	rootCmd.AddCommand(dumpSQLCmd)

	// Add flags
	dumpSQLCmd.Flags().StringVar(&databaseType, "database", "postgresql",
		"Target database type (postgresql, mysql, sqlserver, sqlite)")
	dumpSQLCmd.Flags().BoolVar(&pending, "pending", false,
		"Show SQL for pending schema changes only (requires existing migrations)")
	dumpSQLCmd.Flags().BoolVar(&verbose, "verbose", false,
		"Show detailed processing information")
}
